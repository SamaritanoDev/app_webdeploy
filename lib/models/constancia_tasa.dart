class TableItem {
  final int id;
  final String cod;
  final String entidad;
  final String tasa;
  final String concepto;
  final double costoUnitario;
  final String documento;

  TableItem({
    required this.id,
    required this.cod,
    required this.entidad,
    required this.tasa,
    required this.concepto,
    required this.costoUnitario,
    required this.documento,
  });
}
